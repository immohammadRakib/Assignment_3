-- LOGICAL QUERIES

-- QUERY 1: RETRIEVE ALL UPCOMING FOOTBALL MATCHES BELONGING TO 'CHAMPIONS LEAGUE' WHERE STATUS IS 'AVAILABLE'
SELECT MATCH_ID, FIXTURE, BASE_TICKET_PRICE
FROM MATCHES
WHERE TOURNAMENT_CATEGORY = 'Champions League' 
  AND MATCH_STATUS = 'Available';


-- QUERY 2: SEARCH FOR ALL USERS WHOSE FULL NAMES START WITH 'TANVIR' OR CONTAIN 'HAQUE' (CASE-INSENSITIVE)
SELECT USER_ID, FULL_NAME, EMAIL
FROM USERS
WHERE FULL_NAME LIKE 'Tanvir%' 
   OR FULL_NAME ILIKE '%Haque%';


-- QUERY 3: RETRIEVE BOOKING RECORDS WHERE PAYMENT STATUS IS NULL, REPLACING IT WITH 'ACTION REQUIRED'
SELECT BOOKING_ID, USER_ID, MATCH_ID, 
       COALESCE(PAYMENT_STATUS, 'Action Required') AS SYSTEMATIC_STATUS
FROM BOOKINGS
WHERE PAYMENT_STATUS IS NULL;


-- QUERY 4: RETRIEVE MATCH BOOKING DETAILS ALONG WITH USER'S FULL NAME AND MATCH FIXTURE TEAMS (INNER JOIN)
SELECT B.BOOKING_ID, U.FULL_NAME, M.FIXTURE, B.TOTAL_COST
FROM BOOKINGS B
INNER JOIN USERS U ON B.USER_ID = U.USER_ID
INNER JOIN MATCHES M ON B.MATCH_ID = M.MATCH_ID;


-- QUERY 5: COMPREHENSIVE LIST OF ALL USERS AND BOOKING IDS, INCLUDING FANS WHO NEVER BOUGHT A TICKET (LEFT JOIN)
SELECT U.USER_ID, U.FULL_NAME, B.BOOKING_ID
FROM USERS U
LEFT JOIN BOOKINGS B ON U.USER_ID = B.USER_ID
ORDER BY U.USER_ID;


-- QUERY 6: FIND ALL TICKET BOOKINGS WHERE TOTAL COST IS STRICTLY HIGHER THAN THE AVERAGE COST (SUBQUERY)
SELECT BOOKING_ID, MATCH_ID, TOTAL_COST
FROM BOOKINGS
WHERE TOTAL_COST > (SELECT AVG(TOTAL_COST) FROM BOOKINGS);


-- QUERY 7: RETRIEVE TOP 2 MOST EXPENSIVE MATCHES SORTED BY PRICE, SKIPPING THE ABSOLUTE HIGHEST PREMIUM MATCH
SELECT MATCH_ID, FIXTURE, BASE_TICKET_PRICE
FROM MATCHES
ORDER BY BASE_TICKET_PRICE DESC
LIMIT 2 OFFSET 1;
